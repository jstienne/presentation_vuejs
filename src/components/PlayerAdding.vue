<template>
    <div>
        <input v-model="firstName" placeholder="PrÃ©nom"/>
        <input v-model="surname" placeholder="Nom"/>
        <button @click="addPlayer">Ajouter un joueur</button>
    </div>
</template>

<script>
    export default {
        name: 'PlayerAdding',
        data() {
            return {
                firstName: '',
                surname: ''
            }
        },
        methods: {
            addPlayer() {
                this.$emit('add-player', this.firstName, this.surname);
                this.firstName = '';
                this.surname = '';

            }
        }
    }
</script>